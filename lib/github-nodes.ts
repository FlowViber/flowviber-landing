// Helper function to check if a node type exists
export function isValidN8nNode(nodeType: string): boolean {
  const allNodes = [
    ...N8N_AVAILABLE_NODES.triggers,
    ...N8N_AVAILABLE_NODES.actions,
    ...N8N_AVAILABLE_NODES.langchain,
  ];
  return allNodes.includes(nodeType);
}

// Helper function to check if a node is a trigger
export function isN8nTriggerNode(nodeType: string): boolean {
  return N8N_AVAILABLE_NODES.triggers.includes(nodeType);
}

// Helper function to suggest alternatives for invalid nodes
export function getSuggestedNode(invalidNodeName: string): string {
  const nameLower = invalidNodeName.toLowerCase();
  
  // Common invalid patterns and their fixes
  if (nameLower.includes('send confirmation') || nameLower.includes('confirmation')) {
    return 'n8n-nodes-base.respondToWebhook'; // For sending responses
  }
  if (nameLower.includes('suggest') || nameLower.includes('alternatives')) {
    return 'n8n-nodes-base.switch'; // For conditional logic
  }
  if (nameLower.includes('whatsapp') && nameLower.includes('trigger')) {
    return 'n8n-nodes-base.whatsappTrigger'; // Correct WhatsApp trigger
  }
  if (nameLower.includes('whatsapp')) {
    return 'n8n-nodes-base.whatsapp'; // WhatsApp action node
  }
  if (nameLower.includes('error') && nameLower.includes('handler')) {
    return 'n8n-nodes-base.errorWorkflow'; // Error handling
  }
  if (nameLower.includes('webhook')) {
    return 'n8n-nodes-base.webhook'; // Standard webhook
  }
  
  // Default fallbacks
  if (nameLower.includes('trigger')) {
    return 'n8n-nodes-base.webhook';
  }
  return 'n8n-nodes-base.httpRequest'; // Generic action
}